package com.example.radit.hotcen;

public class DataPoli {
    String idRS, idPoli;

    public DataPoli(String idRS, String idPoli) {
        this.idRS = idRS;
        this.idPoli = idPoli;
    }

    public String getIdRS() {
        return idRS;
    }

    public void setIdRS(String idRS) {
        this.idRS = idRS;
    }

    public String getIdPoli() {
        return idPoli;
    }

    public void setIdPoli(String idPoli) {
        this.idPoli = idPoli;
    }
}

